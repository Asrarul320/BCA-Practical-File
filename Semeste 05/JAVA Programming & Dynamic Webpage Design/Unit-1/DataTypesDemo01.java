// Program to demonstrate the use of different data types in Java
public class DataTypesDemo01 {
    public static void main(String[] args) {
        int age = 25; // Integer data type
        float height = 5.9f; // Floating-point data type
        char grade = 'A'; // Character data type
        String name = "John"; // String data type
        boolean isStudent = true; // Boolean data type

        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Height: " + height);
        System.out.println("Grade: " + grade);
        System.out.println("Is Student: " + isStudent);
    }
}
